function computeProvinceScore(provinceCode: string, clauseStates: Record<string, string>, data: any) {
  const clauses = data[provinceCode]?.clauses || [];
  const active = clauses.filter((c: any) => clauseStates[c.id] !== "ignore");
  if (!active.length) return 0;
  const total = active.reduce((sum: number, c: any) =>
    sum + (clauseStates[c.id] === "limit" ? 0 : c.score), 0);
  return Math.round(total / active.length);
}

function barColor(score: number) {
  if (score >= 75) return "#1D9E75";
  if (score >= 55) return "#BA7517";
  return "#E24B4A";
}

