// SectionRow — collapsible group header with cascade Limit / Ignore buttons
// ---------------------------------------------------------------------------

interface SectionRowProps {
  node: ClauseNode;          // level-0 node
  children: ClauseNode[];    // its direct children
  clauseStates: Record<string, ClauseState>;
  onSetState: (id: string, state: ClauseState) => void;
  onCascade: (parentId: string, state: ClauseState) => void;
  onNavigate: (node: ClauseNode) => void;
}

const SectionRow: React.FC<SectionRowProps> = ({ node, children, clauseStates, onSetState, onCascade, onNavigate }) => {
  const [open, setOpen] = React.useState(true);

  const domState = children.length > 0 ? parentState(children.map((c) => c.id), clauseStates) : clauseStates[node.id];
  const isLimit = domState === S.LIMIT;
  const isIgnore = domState === S.IGNORE;
  const mixed = children.length > 0 && isMixedState(children.map((c) => c.id), clauseStates);

  // For leaf-level-0 nodes (no children), fall back to ClauseRow behaviour
  if (children.length === 0) {
    return <ClauseRow node={node} state={clauseStates[node.id]} onSetState={onSetState} onNavigate={onNavigate} />;
  }

  return (
    <div style={{ marginBottom: 6 }}>
      {/* Section header */}
      <div
        onClick={() => setOpen((o) => !o)}
        style={{
          display: "flex", alignItems: "center", gap: 10,
          padding: "8px 12px",
          borderRadius: 6,
          background: "#f3f2f1",
          border: isLimit
            ? "1px solid #E24B4A"
            : isIgnore
              ? "0.5px dashed #c8c6c4"
              : "0.5px solid #c8c6c4",
          cursor: "pointer",
          userSelect: "none",
        }}
      >
        {/* Chevron */}
        <span style={{
          fontSize: 11, color: "#a19f9d",
          display: "inline-block", transition: "transform 0.2s",
          transform: open ? "rotate(90deg)" : "rotate(0deg)",
          flexShrink: 0,
        }}>▶</span>

        {/* Numbering */}
        <span style={{ fontSize: 11, fontWeight: 500, fontFamily: "monospace", color: "#a19f9d", flexShrink: 0 }}>
          §{node.numbering.replace(".", "")}
        </span>

        {/* Title */}
        <span style={{ fontSize: 14, fontWeight: 500, color: "#323130", flex: 1 }}>
          {node.shortTitle}{node.shortTitle !== node.fullText && "…"}
        </span>

        {/* Mixed badge */}
        {mixed && (
          <span style={{ fontSize: 11, padding: "1px 7px", borderRadius: 20, background: "#FAEEDA", color: "#633806", flexShrink: 0 }}>
            mixed
          </span>
        )}

        {/* Child count */}
        <span style={{ fontSize: 11, color: "#a19f9d", flexShrink: 0 }}>
          {children.length} clause{children.length !== 1 ? "s" : ""}
        </span>

        {/* Cascade buttons */}
        <div style={{ display: "flex", gap: 5, flexShrink: 0 }}>
          <IconButton
            active={isLimit}
            activeColor="#A32D2D"
            activeBg="#FCEBEB"
            label="Limit all clauses in section"
            onClick={() => onCascade(node.id, isLimit ? S.DEFAULT : S.LIMIT)}
          >!</IconButton>
          <IconButton
            active={isIgnore}
            activeColor="#605e5c"
            activeBg="#f3f2f1"
            label="Ignore all clauses in section"
            onClick={() => onCascade(node.id, isIgnore ? S.DEFAULT : S.IGNORE)}
          >✕</IconButton>
        </div>
      </div>

      {/* Child clauses */}
      {open && (
        <div style={{ display: "flex", flexDirection: "column", gap: 4, marginTop: 4, paddingLeft: 8 }}>
          {children.map((child) => (
            <ClauseRow
              key={child.id}
              node={child}
              state={clauseStates[child.id]}
              onSetState={onSetState}
              onNavigate={onNavigate}
            />
          ))}
        </div>
      )}
    </div>
  );
};

