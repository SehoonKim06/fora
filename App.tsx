/** Enforceability bucket per clause, keyed by score 0|1|2. Mirrors clause.forums in Python. */
type ClauseForums = { 0: Set<string>; 1: Set<string>; 2: Set<string> };

/** A single clause node in the document hierarchy. */
interface ClauseNode {
  id: string;
  fullText: string;
  /** First 5 words, used as the display label. */
  shortTitle: string;
  /** 0 = top-level (e.g. "1."), 1 = sub (e.g. "1.1"), 2 = sub-sub (e.g. "1.1.1") */
  level: number;
  /** The numbering prefix extracted from the paragraph, e.g. "1.", "1.1", "2.3.1" */
  numbering: string;
  parentId: string | null;
  childIds: string[];
  /** Human-readable name (= shortTitle). Maps to ClauseNode.name in Python. */
  name: string;
  /**
   * enforce=true  → user wants this clause enforced (DEFAULT state).
   * enforce=false → user wants to limit enforceability (LIMIT state).
   * Mirrors ClauseNode.enforce in Python.
   */
  enforce: boolean;
  /** Per-clause jurisdiction buckets populated after Gemini analysis. */
  forums: ClauseForums;
}

/** Shape of one jurisdiction entry returned by the mock data files. */
interface GeminiJurisdiction {
  province: string;
  enforceability_score: number;
  legal_sources: string[];
  exceptions: string[];
}

/** Full mock response for a single clause. */
interface GeminiClauseResponse {
  clause_id: string;
  jurisdictions: GeminiJurisdiction[];
}

