/** Derive the "dominant" triage state of a parent node from its children's states. */
function parentState(childIds: string[], clauseStates: Record<string, ClauseState>): ClauseState {
  const states = childIds.map((id) => clauseStates[id]);
  if (states.every((s) => s === S.IGNORE)) return S.IGNORE;
  if (states.every((s) => s === S.LIMIT)) return S.LIMIT;
  return S.DEFAULT;
}

/** True when a parent has a mix of states among its children. */
function isMixedState(childIds: string[], clauseStates: Record<string, ClauseState>): boolean {
  const states = childIds.map((id) => clauseStates[id]);
  return states.some((s) => s !== S.DEFAULT) && !states.every((s) => s === states[0]);
}

/** Returns the first 5 words of a clause as a short display label. */
function smartTruncate(text: string, maxWords = 5): string {
  return text.split(/\s+/).slice(0, maxWords).join(" ");
}

