function SummaryCards({ ranked, counts, totalClauses }: any) {
  const top = ranked[0];
  const avgAll = Math.round(ranked.reduce((s: number, r: any) => s + r.score, 0) / ranked.length);
  const cards = [
    { val: top.province.name, label: "Best Forum" },
    { val: top.score, label: "Top score / 100" },
    { val: `${counts.evaluate} / ${totalClauses}`, label: "Clauses evaluated" },
    { val: avgAll, label: "Average score" },
  ];
  return (
    <div style={{ display: "flex", gap: 12, marginBottom: "1.5rem" }}>
      {cards.map((c) => (
        <div key={c.label} style={{
          flex: 1, background: "var(--color-background-secondary)",
          borderRadius: "var(--border-radius-md)", padding: "10px 14px",
        }}>
          <div style={{ fontSize: 18, fontWeight: 500, color: "var(--color-text-primary)" }}>{c.val}</div>
          <div style={{ fontSize: 11, color: "var(--color-text-tertiary)", marginTop: 2 }}>{c.label}</div>
        </div>
      ))}
    </div>
  );
}

