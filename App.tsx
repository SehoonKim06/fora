/** Detect whether a paragraph is a numbered heading and return its level + numbering prefix. */
function detectHeading(text: string): { level: number; numbering: string } | null {
  // Level 2 — e.g. "1.1.1 Some text"
  let m = text.match(/^(\d+\.\d+\.\d+)[\s.]/);
  if (m) return { level: 2, numbering: m[1] };

  // Level 1 — e.g. "1.1 Some text" (not followed by another .digit so we don't grab 1.1.1)
  m = text.match(/^(\d+\.\d+)(?!\.\d)[\s.]/);
  if (m) return { level: 1, numbering: m[1] };

  // Level 0 — e.g. "1. Some text"
  m = text.match(/^(\d+)\.\s/);
  if (m) return { level: 0, numbering: m[1] + "." };

  return null;
}

/** Truncate text to at most `n` words. */
function firstWords(text: string, n = 5): string {
  return text.split(/\s+/).slice(0, n).join(" ");
}

/**
 * Build a flat list of ClauseNodes from raw paragraph strings.
 * Parent–child relationships are determined by heading level transitions.
 */
function buildClauseTree(paragraphTexts: string[]): ClauseNode[] {
  const nodes: ClauseNode[] = [];
  const byId = new Map<string, ClauseNode>();
  const lastAtLevel = new Map<number, ClauseNode>();

  let serial = 0;
  for (const text of paragraphTexts) {
    if (!text.trim()) continue;
    const detected = detectHeading(text);
    if (!detected) continue;

    const { level, numbering } = detected;
    const id = `clause_${serial++}`;

    let parentId: string | null = null;
    if (level > 0) {
      const parent = lastAtLevel.get(level - 1);
      if (parent) parentId = parent.id;
    }

    const node: ClauseNode = {
      id,
      fullText: text,
      shortTitle: firstWords(text, 5),
      level,
      numbering,
      parentId,
      childIds: [],
      name: firstWords(text, 5),
      enforce: true,           // default; updated when user sets LIMIT state
      forums: { 0: new Set(), 1: new Set(), 2: new Set() },
    };

    nodes.push(node);
    byId.set(id, node);
    lastAtLevel.set(level, node);

    // Use Array.from() instead of for...of on Map — required for ES5 targets.
    Array.from(lastAtLevel.keys()).forEach((l) => {
      if (l > level) lastAtLevel.delete(l);
    });

    if (parentId) {
      byId.get(parentId)?.childIds.push(id);
    }
  }

  return nodes;
}

/** Recursively collect all descendant IDs of a node. */
function collectDescendants(nodeId: string, byId: Map<string, ClauseNode>): string[] {
  const node = byId.get(nodeId);
  if (!node || node.childIds.length === 0) return [];
  const result: string[] = [...node.childIds];
  for (const childId of node.childIds) {
    result.push(...collectDescendants(childId, byId));
  }
  return result;
}

